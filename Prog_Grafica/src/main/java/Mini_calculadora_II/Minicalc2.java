    private void CalculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculaActionPerformed
        int x = Integer.parseInt(num1.getText());
        int y = Integer.parseInt(num2.getText());
        String a = null, b, c;
        if (Sumatorio.isSelected()) {
            a = "El resultado del sumatorio es: " + (x + y);
        } else {
            a = "Sumatorio no seleccionado";
        }
        if (Productorio.isSelected()) {
            b = "El resultado del productorio es: " + (x * y);
        } else {
            b = "Productorio no seleccionado";
        }
        if (Exponencia.isSelected()) {
            System.out.println();
            c = "El exponencial es " + Math.pow(x, y);
        } else {

            c = "Exponencial no seleccionado";
        }
        jLabel1.setText(a);
        Etiqueta2.setText(b);
        Etiqueta3.setText(c);
        
                
// TODO add your handling code here:
    }//GEN-LAST:event_CalculaActionPerformed

